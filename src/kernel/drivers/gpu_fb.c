{
    uint32_t *buf = fb.back_buffer ? fb.back_buffer : fb.pixels;
    if (!buf) return;
    for (uint32_t y = 0; y < fb.height; y++) {
        uint32_t *row = (uint32_t *)((uint8_t *)buf + y * fb.pitch);
        fb_memset32(row, color, fb.width);
    }
}

void fb_put_pixel(uint32_t x, uint32_t y, uint32_t color)
{
    if (x >= fb.width || y >= fb.height) return;
    *fb_pixel_addr(x, y) = color;
}

void fb_draw_rect(uint32_t x, uint32_t y, uint32_t w, uint32_t h, uint32_t color)
{
    for (uint32_t row = y; row < y + h && row < fb.height; row++) {
        uint32_t x_end = MIN(x + w, fb.width);
        uint32_t *p = fb_pixel_addr(x, row);
        fb_memset32(p, color, x_end > x ? x_end - x : 0);
    }
}

void fb_draw_rect_outline(uint32_t x, uint32_t y, uint32_t w, uint32_t h,
                          uint32_t color, uint32_t thickness)
{
    /* Top */
    fb_draw_rect(x, y, w, MIN(thickness, h), color);
    /* Bottom */
    if (h > thickness)
        fb_draw_rect(x, y + h - thickness, w, thickness, color);
    /* Left */
    fb_draw_rect(x, y, MIN(thickness, w), h, color);
    /* Right */
    if (w > thickness)
        fb_draw_rect(x + w - thickness, y, thickness, h, color);
}

void fb_draw_line(int32_t x0, int32_t y0, int32_t x1, int32_t y1, uint32_t color)
{
    int32_t dx =  (x1 > x0 ? x1 - x0 : x0 - x1);
    int32_t dy = -(y1 > y0 ? y1 - y0 : y0 - y1);
    int32_t sx = x0 < x1 ? 1 : -1;
    int32_t sy = y0 < y1 ? 1 : -1;
    int32_t err = dx + dy;

    for (;;) {
        fb_put_pixel((uint32_t)x0, (uint32_t)y0, color);
        if (x0 == x1 && y0 == y1) break;
        int32_t e2 = 2 * err;
        if (e2 >= dy) { err += dy; x0 += sx; }
        if (e2 <= dx) { err += dx; y0 += sy; }
    }
}

void fb_draw_char(uint32_t x, uint32_t y, char c, uint32_t fg, uint32_t bg)
{
    if (c < 32 || c > 126) c = '?';
    const uint8_t *glyph = font8x16[(int)c - 32];

    for (uint32_t row = 0; row < FB_FONT_H; row++) {
        uint8_t bits = glyph[row];
        for (uint32_t col = 0; col < FB_FONT_W; col++) {
            uint32_t color = (bits & (0x80 >> col)) ? fg : bg;
            fb_put_pixel(x + col, y + row, color);
        }
    }
}

void fb_draw_string(uint32_t x, uint32_t y, const char *s, uint32_t fg, uint32_t bg)
{
    uint32_t cx = x;
    while (*s) {
        if (*s == '\n') {
            cx = x;
            y += FB_FONT_H;
        } else {
            fb_draw_char(cx, y, *s, fg, bg);
            cx += FB_FONT_W;
        }
        s++;
    }
}

void fb_scroll(uint32_t region_y, uint32_t region_h, uint32_t lines)
{
    uint32_t *buf = fb.back_buffer ? fb.back_buffer : fb.pixels;
    if (!buf || lines == 0 || lines >= region_h) {
        /* Clear the whole region */
        for (uint32_t y = region_y; y < region_y + region_h && y < fb.height; y++) {
            uint32_t *row = (uint32_t *)((uint8_t *)buf + y * fb.pitch);
            fb_memset32(row, 0x00000000, fb.width);
        }
        return;
    }

    uint32_t pixel_scroll = lines;
    /* Move rows up */
    for (uint32_t y = region_y; y < region_y + region_h - pixel_scroll; y++) {
        uint32_t *dst = (uint32_t *)((uint8_t *)buf + y * fb.pitch);
        uint32_t *src = (uint32_t *)((uint8_t *)buf + (y + pixel_scroll) * fb.pitch);
        fb_memcpy(dst, src, fb.width * 4);
    }
    /* Clear the newly revealed rows at bottom */
    for (uint32_t y = region_y + region_h - pixel_scroll; y < region_y + region_h; y++) {
        if (y >= fb.height) break;
        uint32_t *row = (uint32_t *)((uint8_t *)buf + y * fb.pitch);
        fb_memset32(row, 0x00000000, fb.width);
    }
}

void fb_blit(uint32_t dst_x, uint32_t dst_y,
             const uint32_t *src, uint32_t src_w, uint32_t src_h)
{
    if (!src) return;
    for (uint32_t y = 0; y < src_h && (dst_y + y) < fb.height; y++) {
        for (uint32_t x = 0; x < src_w && (dst_x + x) < fb.width; x++) {
            *fb_pixel_addr(dst_x + x, dst_y + y) = src[y * src_w + x];
        }
    }
}

