static font_t default_font;
static bool   font_initialized = false;

void font_init(void)
{
    default_font.width      = FONT_WIDTH;
    default_font.height     = FONT_HEIGHT;
    default_font.glyph_data = font_bitmap_data;
    default_font.first_char = 32;
    default_font.last_char  = 126;
    font_initialized = true;
}

font_t *font_get_default(void)
{
    if (!font_initialized)
        font_init();
    return &default_font;
}

void font_draw_char(fb_ctx_t *fb, int x, int y, char c,
                    uint32_t fg, uint32_t bg, const font_t *font)
{
    if (!fb || !font || !font->glyph_data)
        return;

    uint8_t ch = (uint8_t)c;
    if (ch < font->first_char || ch > font->last_char)
        ch = '?';

    int idx = (ch - font->first_char) * font->height;
    const uint8_t *glyph = &font->glyph_data[idx];

    for (int row = 0; row < font->height; row++) {
        uint8_t bits = glyph[row];
        for (int col = 0; col < font->width; col++) {
            int px = x + col;
            int py = y + row;
            if (px < 0 || py < 0 ||
                (uint32_t)px >= fb->width || (uint32_t)py >= fb->height)
                continue;

            uint32_t color = (bits & (0x80 >> col)) ? fg : bg;
            if (color != 0)
                fb_put_pixel(px, py, color);
        }
    }
}

void font_draw_string(fb_ctx_t *fb, int x, int y, const char *text,
                      uint32_t fg, uint32_t bg, const font_t *font)
{
    if (!text || !font)
        return;

    int cx = x;
    while (*text) {
        if (*text == '\n') {
            cx = x;
            y += font->height;
        } else {
            font_draw_char(fb, cx, y, *text, fg, bg, font);
            cx += font->width;
        }
        text++;
    }
}

int font_draw_string_wrap(fb_ctx_t *fb, int x, int y, int max_width,
                          const char *text, uint32_t fg, uint32_t bg,
                          const font_t *font)
{
    if (!text || !font || max_width <= 0)
        return 0;

    int chars_per_line = max_width / font->width;
    if (chars_per_line <= 0)
        chars_per_line = 1;

    int cx = x;
    int cy = y;
    int lines = 1;
    int col = 0;

    const char *word_start = text;
    int word_len = 0;

    while (*text) {
        if (*text == '\n') {
            cx = x;
            cy += font->height;
            col = 0;
            lines++;
            text++;
            continue;
        }

        if (*text == ' ') {
            if (fb)
                font_draw_char(fb, cx, cy, ' ', fg, bg, font);
            cx += font->width;
            col++;
            if (col >= chars_per_line) {
                cx = x;
                cy += font->height;
                col = 0;
                lines++;
            }
            text++;
            continue;
        }

        word_start = text;
        word_len = 0;
        while (*text && *text != ' ' && *text != '\n') {
            word_len++;
            text++;
        }

        if (col + word_len > chars_per_line && col > 0) {
            cx = x;
            cy += font->height;
            col = 0;
            lines++;
        }

        for (int i = 0; i < word_len; i++) {
            if (col >= chars_per_line) {
                cx = x;
                cy += font->height;
                col = 0;
                lines++;
            }
            if (fb)
                font_draw_char(fb, cx, cy, word_start[i], fg, bg, font);
            cx += font->width;
            col++;
        }
    }

    return lines;
}

int font_measure_string(const char *text, const font_t *font)
{
    if (!text || !font)
        return 0;

    int len = 0;
    int max_len = 0;
    while (*text) {
        if (*text == '\n') {
            if (len > max_len) max_len = len;
            len = 0;
        } else {
            len++;
        }
        text++;
    }
    if (len > max_len) max_len = len;

    return max_len * font->width;
}

int font_measure_string_wrapped(const char *text, int max_width,
                                const font_t *font)
{
    if (!text || !font || max_width <= 0)
        return 0;

    int lines = font_draw_string_wrap(NULL, 0, 0, max_width, text, 0, 0, font);
    return lines * font->height;
}
